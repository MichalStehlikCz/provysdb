package com.provys.provysdb.dbcontext;

import java.sql.Connection;
import java.sql.SQLException;

/**
 * Provys specific wrapper on connection. Returns Provys specific wrappers on statements, that
 * provide retrieval of application specific data-types
 */
public interface DbConnection extends Connection {

  @Override
  DbStatement createStatement() throws SQLException;

  @Override
  DbStatement createStatement(int resultSetType, int resultSetConcurrency) throws SQLException;

  @Override
  DbPreparedStatement prepareStatement(String sql) throws SQLException;

  @Override
  DbPreparedStatement prepareStatement(String sql, int resultSetType, int resultSetConcurrency)
      throws SQLException;

  @Override
  DbPreparedStatement prepareStatement(String sql, int autoGeneratedKeys) throws SQLException;

  @Override
  DbPreparedStatement prepareStatement(String sql, int[] columnIndexes) throws SQLException;

  @Override
  DbPreparedStatement prepareStatement(String sql, String[] columnNames) throws SQLException;

  @Override
  DbCallableStatement prepareCall(String sql) throws SQLException;

  @Override
  DbCallableStatement prepareCall(String sql, int resultSetType, int resultSetConcurrency)
      throws SQLException;

  @Override
  DbCallableStatement prepareCall(String sql, int resultSetType, int resultSetConcurrency,
      int resultSetHoldability) throws SQLException;
}
